package elmos.oxono.model;

/**
 * Enum representing the possible directions for a piece.
 * This enum defines two directions: {@link Direction#HORIZONTAL} and {@link Direction#VERTICAL}.
 */
public enum Direction {
    /**
     * Represents the horizontal direction.
     */
    HORIZONTAL,

    /**
     * Represents the vertical direction.
     */
    VERTICAL;
}

